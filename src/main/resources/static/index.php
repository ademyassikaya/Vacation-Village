                <h2 class="hakkimizda mt-5" style="text-align:center; font-family: avenir; font-size: 30px; font-weight: 200;">HAKKIMIZDA</h2>
               <p class="uk-text-left@m"></br>Yıllardır adını duyduğunuz Oto Anka, yeni şubesinde otomobil performanslarına odaklanarak Oto Anka Performans'a evriliyor. Sizlerin otomobil performansı konusunda isteklerini karşılamak için dünyanın en iyi markaları ile ortaklıklar kurarak, otomobillerinizi sizlerin istekleri doğrultusunda karakterize ediyoruz. Superchips, Sprintbooster, Supersprint, Volkswagen racing, BMC Air Filter, APR gibi alanlarında tartışmasız olarak 1 numara olan markalar ile otomobillerinizi farklı boyutlara taşıyoruz.

</br>Performans noktasında; aracınızı Superchips Chip Tuning ile güçlendirirken, yanında Chip Tuning işlemini anlamlı kılacak Sprintbooster ve BMC Hava Filtresi gibi ürünleri sizlerin imkanına sunarak, Chip Tuning işlemini bir bütün haline getiriyoruz ve aracınızı hem daha sağlıklı hem daha güçlü bir hale dönüştürüyoruz.

</br>Her bir araç için özel olarak tasarlanmış olan Supersprint egzozları ile aracınızı diğerlerinden tamamen ayırarak karakteristik bir hava katıyoruz.

</br>Sadece VAG grubu araçlar için, yüksek mühendislik teknolojileriyle üretilen APR ve Volkswagen Racing ürünleriyle; Scoda, Seat, Volkswagen, Audi araçlarını sizlerin istekleri doğrultusunda çok yüksek performanslar alabileceğiniz bir hale dönüştürebiliyoruz.

</br>Sizler için bir araya gelen 6 büyük marka; Superchips, Sprintbooster, Supersprint, Volkswagen Racing, BMC Air Filter, APR her türlü bütçeye uygun, güvenilirliği tüm dünya piyasasında kabul gören ve artık çok kolay erişebilirliği ile sizlerin otomobil performansı konusunda her türlü isteğinizi karşılamaya hazır olarak Oto Anka'da.

</br>Ürün gamı açısından çok büyük skalaya sahip olan bu markalar ile chip tuning yazılımları, egzoz sistemleri, hava filtreleri, down pipelar, intercoolerlar gibi bir çok ürünler ile araçlarınızı en baştan sizlerin istekleri doğrultusunda en güvenli, en ekonomik ve en kaliteli şekilde baştan yaratırıyoruz.

</br>Otomobil performansı konusunda sınırsız imkanların olduğu, tecrübeli ve güvenilir ekibiyle Oto Anka Performans yeni şubesi Ümitköy'de!

